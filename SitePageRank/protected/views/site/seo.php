<h1>提升網站能見度</h1>

<p>網站資料要除了要讓人類使用者容易閱讀，也需要讓機器(搜尋引擎）看得懂，其他人才容易透過搜尋找到您的網站<br>除此之外網站要讓使用者容易閱讀，且載入速度快，實用好讀的內容容易被分享出去。</p>
<h2>搜尋如何運作</h2>
