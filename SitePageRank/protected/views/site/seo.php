<h2>關鍵重點</h2>

<h4>網頁內容</h4>
<ul>
	<li>增加實用網站內容，例如專業領域專欄或專業訊息，網站有好內容才容易被使用及分享。</li>
	<li>使用者讀取方向習慣性為Z的順序讀取，儘量依照使用者習慣安排版面。</li>
	<li>標題與內文需明顯區分</li>
	<li>儘量精簡文字，非必要的文字就是不必要的文字。</li>
	<li>內容之間要留空白，舒緩視覺。</li>
	<li>扁平化設計。</li>
	<li>按鈕與內容要明顯區別。</li>
	<li>別讓使用者迷路。</li>
	<li>重要內容、選單、圖片不使用flash呈現，雖然google讀得到部分flash內容。<br>另一個不用flash的原因是多數的平板及手機無法呈現flash</li>	
</ul>
<h4>網頁標籤類</h4>
<ul>
	<li><code>title</code>重要關鍵字要往前面放，中文總長度不超過40個字，可用-分隔，例如「人事室-國立高雄應用科技大學」</li>
	<li><code>Meta Description Tag</code>關鍵字建議前面十個字就出現，整體中文長度建議40個字內，頁面不重複。</li>
	<li><code>Facebook Open Graph</code>給FB抓取的縮圖。</li>
	<li><code>Twitter Cards</code>調整twiter抓取的樣式</li>
	<li>使用<code>Canonical</code>減少網頁重複被索引，並指引索引正確的頁面。</li>
	<li>正確的使用<code>tabel</code>與<code>div</code>，<code>table</code>用於呈現資料而非排版，<code>div</code>用於排版。</li>
</ul>
<h4>其他</h4>
<ul>
	<li>網站申請高應大網址，相關<a href="http://www.kuas.edu.tw">表單下載</a>。</li>
	<li>網站連結掛在<a href="http://www.kuas.edu.tw">高應網站</a>底下。</li>

	<li>加快網站回應速度(回應速度必須小於200ms)</li>
	<li>重要頁面、選單不使用Ajax產生，用Ajax產生的頁面需要特別處理，<a href="https://developers.google.com/webmasters/ajax-crawling/">參考資料。</a></li>
	<li>安裝Google Analistic 至網站所有頁面，分析使用者行為。</li>
	<li>提交SiteMap.xml，讓搜尋引擎可以正確快取網站資料。</li>
	<li>內容放置在正確的網頁標籤中。</li>
	<li>經營Facebook、Twiter社群媒體，將網站頁面網址轉貼上去，搜尋引擎容易透過社群媒體找到轉帖的內容。</li>
	<li>買Google廣告</li>
</ul>
