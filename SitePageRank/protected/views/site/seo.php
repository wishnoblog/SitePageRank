<h1>提升網站能見度</h1>

<p>網站資料要除了要讓人類使用者容易閱讀，也需要讓機器(搜尋引擎）看得懂，這篇文章是關於如何調整網站系統。</p>

<h2>關鍵重點</h2>

<ul>
	<li>網站申請高應大網址，<a href="http://www.kuas.edu.tw">表單下載</a>。</li>
	<li>網站掛在<a href="http://www.kuas.edu.tw">高應大主要網站</a>底下。</li>
	<li>重要內容、圖片不使用FLASH呈現，尤其是網站進入第一頁，Flash的內容不會被搜尋引擎解析。</li>
	<li>網址部分需要做管理，需要做好url Route，例如 <code>file/1-1-a.php</code> 整理成<code> /排行 </code></li>
	<li>加快網站回應速度(回應速度必須小於200ms)</li>
	<li>重要頁面不使用Ajax產生(Ex)，用Ajax產生的頁面有很大機會搜尋引擎機器人會看不懂而略過索引。</li>
	<li>安裝Google Analistic 至網站所有頁面，分析使用者行為。</li>
	<li>提交SiteMap.xml，讓搜尋引擎可以正確快取網站資料。</li>
	<li>圖片加上Title標簽。</li>
	<li>使用Div Layout</li>
	<li>經營Facebook、Twiter社群媒體，將網站頁面網址轉貼上去，搜尋引擎容易透過社群媒體找到轉帖的內容。</li>
	<li>增加網站內容，例如專業領域專欄或專業訊息。</li>
	<li>買Google廣告</li>
</ul>
